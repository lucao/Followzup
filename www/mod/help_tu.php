<h2>Quem Somos</h2>

<p>Somos uma instituição profissional sediada na cidade do Recife, Estado de Pernambuco - Brasil, gestora de um modelo de negócio voltado para troca de mensagens na modalidade Business-to-People (B2P).

<br><br>
<h3>Missão</h3>

<p>Criar e manter um sistema gratuito e de protocolo aberto, para envio de mensagens pela da Internet, promovendo a comunicação entre aplicações e seus usuários, e a participação de desenvolvedores de sistemas e fabricantes de equipamentos na criação e melhoria de interfaces para os mais diversos dispositivos e públicos.

<br><br>
<h3>Visão</h3>

<p>Ser uma instituição reconhecida no ramo de comunicação pela Internet, agregando valor e promovendo projetos de sucesso e de destaque no meio virtual.

<br><br>
<h3>Valores</h3>

<ul><li><b>Ética</b> - Acreditamos que a responsabilidade, a retidão, a integridade, a honestidade, o respeito às diferenças e o senso de justiça sejam o caminho a ser seguido pelas pessoas em suas relações pessoais e profissionais;<br><br></li>

<li><b>Transparência</b> - Acreditamos que o compartilhamento do conhecimento e a adoção de processos claros e objetivos são essenciais para o sucesso do desenvolvimento social, sendo elementos catalizadores para a participação de todos;<br><br></li>

<li><b>Socialização de resultados</b> - Acreditamos que o sucesso e o desenvolvimento das instituições passam pela socialização de seus resultados, promovendo o crescimento não apenas de suas organizações, mas também de seus colaboradores, pela participação efetiva em seu desenvolvimento;<br><br></li>

