<h2>Exemplos de uso</h2>

<p>Pela <b>praticidade</b> do modelo, podemos relacionar várias de suas utilidades. No aspecto gerencial, em relação ao monitoramento de recursos, podemos configurar nossos sistemas para enviar mensagens ao administrador de um website quando alguém utiliza a página "Entre em Contato", ou ao gerente de produção quando o processo crítico de determinado sistema sofre algum tipo de interrupção, ou à equipe de suporte quando um circuito de dados sai fora do ar, ou a um funcionário de vendas quando recebe o e-mail de um cliente especial, ou a um gerente de compras quando o estoque de determinado produto alcança seu ponto crítico, enfim, <b>tudo que precisamos gerenciar</b>.

<p>Pela <b>segurança</b> do processo, podemos também relacionar muitas outras utilidades, tais como o envio de códigos de barras para pagamento de contas, confirmação de pedidos, agendamento de audiências e compromissos, alertas de compras realizadas com cartões de crédito, confirmação de troca de senhas, avisos de ligações não atendidas, resultados de exames clínicos, movimentações financeiras, avisos de vencimento de apólices de seguros, alertas de alterações de cadastros, avisos de interrupção de serviços, rastreamento de encomendas, enfim, <b>tudo que precisamos ser informados</b>.

<p>Por sua <b>conveniência</b>, outras muitas utilidades podem ser exemplificadas, tais como: previsões astrológicas, salmos, tábuas de marés, avisos de estréias em cinemas e teatros, avisos de promoções de passagens aéreas, notícias sobre temas específicos (clipping), avisos de liquidações, avisos de ofertas de produtos em sites de compras, resultados de loterias, confirmação de vôos, notas de provas, calendários de eventos, enfim, <b>tudo que possa nos auxiliar em nosso dia a dia</b>.

